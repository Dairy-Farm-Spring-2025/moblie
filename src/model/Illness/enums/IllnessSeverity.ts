export enum IllnessSeverity {
  /**
   * When no need to treatment.
   */
  none = 'none',

  /**
   * Represents a mild illness, usually less severe and treatable.
   */
  mild = 'mild',

  /**
   * Represents a moderate illness, where symptoms are more noticeable and treatment may be required.
   */
  moderate = 'moderate',

  /**
   * Represents a severe illness, indicating a more serious condition that requires immediate care.
   */
  severe = 'severe',

  /**
   * Represents a critical illness, where the cow's health is at great risk and urgent medical attention is needed.
   */
  critical = 'critical',
}
